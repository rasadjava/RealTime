package com.in.c2n.model;

public class AppointmentCount {

	private int doctorid;
	private String bookingcountdate;
	private int count;
	public int getDoctorid() {
		return doctorid;
	}
	public void setDoctorid(int doctorid) {
		this.doctorid = doctorid;
	}
	public String getBookingcountdate() {
		return bookingcountdate;
	}
	public void setBookingcountdate(String bookingcountdate) {
		this.bookingcountdate = bookingcountdate;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
}
